Bayview Family Practice & Baraga County Medical Practice
Our engagement with Bayview Family Practice & Baraga County Medical Practice began with a gap analysis of their current implementation of CPSI and Meaningful Use/PCMH EHR related standards. The practices were not on the ONC-ATCB certified version of CPSI, so the project plan for this practice included upgrading to the certified version and implementing the workflow changes necessary to incorporate the MU and PCMH EHR related measures.  M-CEITA is currently working with the practice on implementing Cielo as their patient registry as part of our gap analysis identified several deficiencies in the patient registry functionality of CPSI. Part of this process has involved eliminating duplicative efforts and streamlining the use of Cielo with their EHR.
